    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int id = Integer.parseInt(txtc.getText());
        String nombre = txtn.getText();
        double cantidad = Double.parseDouble(txtp.getText());
        String marca = txtm.getText();
        File ruta = new File(txtruta.getText());
        
        if(ruta!=null){
            this.modificar(nombre, cantidad, marca, ruta, id);
            t.visualizar_ProductoVO(tabla);
            limpiar();
        }else{
            this.modificar2(nombre, cantidad, marca,id);
            t.visualizar_ProductoVO(tabla);
            limpiar();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

