    public void agregar(String nombre, double cantidad, String marca, File ruta){
        ProductoVO vo = new ProductoVO();
        dao = new ProductoDAO();
        
        vo.setNombre(nombre);
        vo.setCantidad(cantidad);
        vo.setMarca(marca);
        try{
            byte[] icono = new byte[(int) ruta.length()];
            InputStream input = new FileInputStream(ruta);
            input.read(icono);
            vo.setFoto(icono);
        }catch(Exception ex){
            vo.setFoto(null);
        }
        dao.Agregar_ProductoVO(vo);
        limpiar();
    }
    
    public void modificar(String nombre, double cantidad, String marca, File ruta, int id){
        ProductoVO vo = new ProductoVO();
        dao = new ProductoDAO();
        
        vo.setIdproducto(id);
        vo.setNombre(nombre);
        vo.setCantidad(cantidad);
        vo.setMarca(marca);
        try{
            byte[] icono = new byte[(int) ruta.length()];
            InputStream input = new FileInputStream(ruta);
            input.read(icono);
            vo.setFoto(icono);
        }catch(Exception ex){
            vo.setFoto(null);
        }
        dao.Modificar_ProductoVO(vo);
    }
    
